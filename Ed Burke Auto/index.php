                I was able to hire a full time technician to help with the work load. In 2011 after three years at the
                current shop, my oldest son graduated from high school and came to work full time.
                2013 my youngest son joined the mix after he graduated high school.<br> In 2014 I purchased my current
                shop.
                We completely gutted the facility in June of 2016 to do renovations which included a new downdraft booth
                that was equipped to spray our water based paints.
                January 23rd 2017 we opened for business at the new shop. My crew and I can handle all types of
                collision repair, but we still specialize in dealership work.
                We understand the need for rapid turnaround at a reasonable price so we do our best to accommodate. We
